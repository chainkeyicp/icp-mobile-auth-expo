import { execFileSync } from 'node:child_process';
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const distDir = path.join(projectRoot, 'mobile-auth', 'dist');
const outputFile = path.join(projectRoot, 'src', 'mobile_auth_canister', 'Assets.mo');

const packageName = process.env.ANDROID_PACKAGE_NAME ?? 'com.example.icpmobileauth';
const fingerprints = readFingerprints();

const generatedAssets = [
  ...(await readDistAssets()),
  {
    path: '/.well-known/assetlinks.json',
    contentType: 'application/json; charset=utf-8',
    cacheControl: 'no-store',
    body: Buffer.from(JSON.stringify(assetLinksJson(), null, 2))
  },
  {
    path: '/auth-callback/index.html',
    contentType: 'text/html; charset=utf-8',
    cacheControl: 'no-store',
    body: Buffer.from(authCallbackFallbackHtml())
  }
];

await fs.writeFile(outputFile, renderMotoko(generatedAssets));
console.log(`Generated ${path.relative(projectRoot, outputFile)} with ${generatedAssets.length} assets.`);

async function readDistAssets() {
  const files = await walk(distDir);

  return Promise.all(
    files.map(async filePath => {
      const relative = path.relative(distDir, filePath).replace(/\\/g, '/');
      const canisterPath =
        relative === 'index.html' ? '/mobile-auth/index.html' : `/mobile-auth/${relative}`;

      return {
        path: canisterPath,
        contentType: contentTypeFor(filePath),
        cacheControl: relative === 'index.html' ? 'no-store' : 'public, max-age=31536000, immutable',
        body: await fs.readFile(filePath)
      };
    })
  );
}

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walk(entryPath)));
    } else {
      files.push(entryPath);
    }
  }

  return files;
}

function readFingerprints() {
  const configured = process.env.ANDROID_SHA256_CERT_FINGERPRINTS;
  if (configured) {
    return configured
      .split(',')
      .map(value => value.trim())
      .filter(Boolean);
  }

  const debugFingerprint = readDebugKeystoreFingerprint();
  if (debugFingerprint) {
    return [debugFingerprint];
  }

  return ['REPLACE_WITH_RELEASE_OR_PLAY_APP_SIGNING_SHA256'];
}

function readDebugKeystoreFingerprint() {
  try {
    const output = execFileSync(
      'keytool',
      [
        '-list',
        '-v',
        '-keystore',
        path.join(projectRoot, 'android', 'app', 'debug.keystore'),
        '-alias',
        'androiddebugkey',
        '-storepass',
        'android',
        '-keypass',
        'android'
      ],
      { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }
    );

    return output.match(/SHA256:\s*([A-F0-9:]+)/i)?.[1]?.toUpperCase();
  } catch {
    return null;
  }
}

function assetLinksJson() {
  return [
    {
      relation: ['delegate_permission/common.handle_all_urls'],
      target: {
        namespace: 'android_app',
        package_name: packageName,
        sha256_cert_fingerprints: fingerprints
      }
    }
  ];
}

