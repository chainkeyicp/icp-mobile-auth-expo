function renderMotoko(assets) {
  return `// Generated by scripts/generate-auth-canister-assets.mjs.
// Do not edit by hand.
module {
  public type Asset = {
    path : Text;
    contentType : Text;
    cacheControl : Text;
    body : [Nat8];
  };

  public let entries : [Asset] = [
${assets.map(renderAsset).join(',\n')}
  ];
};
`;
}

function renderAsset(asset) {
  return `    {
      path = ${motokoText(asset.path)};
      contentType = ${motokoText(asset.contentType)};
      cacheControl = ${motokoText(asset.cacheControl)};
      body = [
${renderBytes(asset.body)}
      ];
    }`;
}

function renderBytes(buffer) {
  const values = [...buffer].map(byte => `${byte}`);
  const lines = [];

  for (let index = 0; index < values.length; index += 16) {
    lines.push(`        ${values.slice(index, index + 16).join('; ')}`);
  }

  return lines.join(',\n').replace(/;/g, ',');
}

function motokoText(value) {
  return `"${value.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

function contentTypeFor(filePath) {
  if (filePath.endsWith('.html')) {
    return 'text/html; charset=utf-8';
  }
  if (filePath.endsWith('.js')) {
    return 'text/javascript; charset=utf-8';
  }
  if (filePath.endsWith('.css')) {
    return 'text/css; charset=utf-8';
  }
  if (filePath.endsWith('.svg')) {
    return 'image/svg+xml';
  }
  return 'application/octet-stream';
}
